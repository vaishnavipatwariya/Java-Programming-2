public class DoubleDataType {
    public static void main(String[] args) {
        double x = 5;
        double y = 2;
        double z = x / y;
        System.out.println(z);
        z = 5 / 2;
        System.out.println(z);
        z = 5.0 / 2;
        System.out.println(z);
        z = 5 / 2.0;
        System.out.println(z);
        z = 5.0 / 2.0;
        System.out.println(z);
    }
}
