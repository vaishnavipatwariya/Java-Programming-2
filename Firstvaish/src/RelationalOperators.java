public class RelationalOperators {
    public static void main(String[] args){
        int p = 10 , q = 15;

        // == is equal to
        // ! not equal to
        // > greater than
        // < less than
        // >= greater than or equal to
        // <= lesser than or equal to

        System.out.println(p == q); //false
        System.out.println(p != q); //true
        System.out.println(p <= q); //true
        System.out.println(p >= q); //false
        System.out.println(p < q);  //true
        System.out.println(p > q);  //false

    }
}