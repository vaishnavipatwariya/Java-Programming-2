package conditionals;

public class SwitchCaseExample {
    public static void main(String[] args){
        int course = 2;
        switch (course){
            case 1 :
                System.out.println("JAVA");
                break;
            case 2:
                System.out.println("PYTHON");
                break;
            default:
                System.out.println("CPP");
                



        }
    }

}
